      <div class="col-md-12">
	  <h3 class="package-heading">Day 1: Delhi- Jodhpur </h3>
	  <p>Flight to Delhi and on to Jodhpur (departs 0950, arrives Jodhpur at 1330) You’ll be met on arrival in Jodhpur 
	  and make a tour of Meherangarh Fort Overnight in Jodhpur at Pal Haveli </p> 
	  
	  <h3 class="package-heading">Day 2: Jodhpur- Jaisalmer </h3>
	  
	  <p>Free time in the morning in Jodhpur before driving 4 hours to Jaisalmer- overnight in Jaisalmer at Deoki Niwas </p>

	  
<h3 class="package-heading">Day 3: Jaisalmer</h3>
	 
<p>Spend a full day around Jaisalmer, most of which will be spent exploring the amazing Fort here, and the glorious sandstone Havelis 
(traditional homes) which are clustered around the base of the Fort. Jaisalmer is something of a ‘frontier town’ and has an atmosphere 
unlike that of any of the other cities of Rajasthan. Overnight at Deoki Niwas  </p>

<h3 class="package-heading">Day 4: Jaisalmer- Rohet  </h3>
	 
<p>After breakfast, leave Jaisalmer and drive 4 to the Wilderness Camp at Rohet where you’ll stay in a tented encampment in the heart of the desert. 
Time in the afternoon to walk and meet local people and enjoy the stark and compelling scenery of this region. Overnight at Rohet Wilderness camp</p>

<h3 class="package-heading">Day 5: Rohet  </h3>
	 
<p>The major activity today will be a jeep safari from the camp to visit villages of the Bishnoi people who are a semi-nomadic tribal group with a 
lifestyle uniquely adapted to their harsh and unforgiving surroundings. The desert is also home to a variety of wildlife including gazelles, 
foxes and jackals. Overnight at Rohet Wilderness camp </p>

<h3 class="package-heading">Day 6: Rohet- Ranakpur </h3>
	 
<p>Continue south to Ranakpur and your accommodation in a lushly forested valley in the Aravali Hills. The contrast between the arid deserts of 
the last couple of days couldn’t be more pronounced- this is one of the greenest areas of Rajasthan. The west of the country was at one time dominated by 
the Jain religion and the temples at Ranakpur reflect this golden age of Jain temple building. One of the central tenets of the religion is that inner 
beauty should be cultivated with little regard given for outward appearance and the temples are a good example of this. From the outside they are 
relatively plain, giving little hint of what lies inside- spectacular carving, intricate design and a staggering level of craftsmanship. 
Tour the complex with a guide and stay overnight near to Ranakpur at Aranyawas </p>

<h3 class="package-heading">Day 7: Ranakpur- Udaipur</h3>
	 
<p>Depending on your propensity to enjoy forts you may want to detour en route to Udaipur and stop in at Kumbhalgarh. (Personally I rate this as the best 
‘fort experience’ in Rajasthan- not only is it vast and imposing but being a reasonable distance off the main tourist trail there’s always a chance you’ll 
find the place largely deserted. Total driving time is about 3 hours with an extra hour or so to include Kumbhalgarh. Overnight in Udaipur at Karohi Haveli </p>

<h3 class="package-heading">Day 8: Udaipur </h3>
<p>A full day in Udaipur- India’s ‘most romantic city’ - with a highlights of your day’s sightseeing including the City Palace, Jagniwas Island on 
the lake and the Jagannath Temple in the heart of the old city. You’ll visit all these during the day as well as having time to wander in the mazelike 
streets of the old city. Udaipur is famous for silversmiths, painters (the Udaipur miniature style is still widely practiced) and classical
music performances. A slightly more recent additional to the cultural milieu is that the 1980s James Bond movie Octopussy was filmed in and around 
Udaipur- this is seen as a matter of great civic pride in some circles and the film (on very old and worn VHS copies) is screened nightly in many 
restaurants (although not at your hotel!) Overnight in Udaipur at Karohi Haveli</p>

<h3 class="package-heading">Day 9: Udaipur- Jojawar </h3>
<p>From Udaipur head to Jojawar and take the ‘toy train’ ride in the afternoon. This old railway line runs up into the Aravali Hills and is a 
most unusual and interesting way to travel Overnight at Rawla Jojawar </p>

<h3 class="package-heading">Day 10: Jojawar- Pushkar</h3>
<p>After breakfast depart Jojawar and head to the sacred lakeside town of Pushkar. Join a guide in the afternoon for sightseeing; 
Pushkar is home to one of only 2 temples in India dedicated to Brahma- this makes the town a very important site for pilgrims and spiritual 
seekers flock here from throughout India and around the world. The atmosphere in Pushkar is relaxed; the pace relatively slow and the streets 
are largely free of traffic, making it a great place to walk around on foot.  Overnight in Pushkar at Pushkar Palace </p>

<h3 class="package-heading">Day 11: Pushkar- Jaipur</h3>
<p>Depart Pushkar after breakfast and drive 3 hours to Jaipur, the ‘Pink City’ and capital of Rajasthan. No set programme for the rest of the 
day although if you fancy the idea of catching a Bollywood movie while you’re in India there are few better places than the Raj Mandir in Jaipur- 
the most sumptuous cinema in the country. You might also like to make a foray into the walled city of (old) Jaipur as well- this is perhaps the most 
extensive market area in India- entire streets are given over to one very specific type of product- the next street will be selling something completely 
different. As much fun for non-shoppers as for keen bargain hunters! Overnight in Jaipur at Shahpura House</p>

<h3 class="package-heading">Day 12:  Jaipur </h3>
<p>Sightseeing around Jaipur including a tour of Amber Fort- you can ride an elephant or take a jeep to the top of the ramparts- 
before heading into the heart of the city and the visiting the Palace and nearby 18th century astronomical observatory- Jantar Mantar- home 
to the world’s largest sundial and other curiosities. (Local food tip- even though it won’t be a hot day, make sure you try the kulfi in the 
area around the palace. This buttermilk-based, cardamom flavoured ice cream is always good but here it is the best in India) Further free time 
in the afternoon, overnight in Jaipur at Shahpura House </p>

<h3 class="package-heading">Day 13:  Jaipur- Agra</h3>
<p>After breakfast leave Jaipur and drive 5 hours to Agra, with a stop en route at Fatehpur Sikri. This abandoned Moghul capital was 
only occupied for about 15 years in the late 16th century and remains in a fantastic state of preservation. Tour the site with a guide and 
then continue on to Agra. Visit Agra Fort and other Mughal-era monuments in the afternoon. Overnight in Agra at Howard Park Plaza Hotel</p>

<h3 class="package-heading">Day 14:  Agra- Gwailior- Orchha</h3>
<p>Visit the Taj at dawn with a guide- best to allow around 3 hours, and then depart for Gwalior and Orchha, arriving late in the day. 
Overnight in Orchha at Bundelkhand Resort </p>

<h3 class="package-heading">Day 15:  Orchha- Varanasi overnight train </h3>
<p>A very relaxing day around Orchha with time to explore this historic and picturesque town fully before driving back to 
Jhansi (30 minutes) for a train to Varanasi- Bundelkhand Express Train #1107 departs 2230. Overnight on the train in 2nd class 
air-conditioned sleeper carriage </p>

<h3 class="package-heading">Day 16:  Varanasi </h3>
<p>On arrival in Varanasi at 1110, you’ll be met and transferred to the city and check in at your accommodation. 
Sightseeing in the afternoon including Sarnath- an important Buddhist site- and several temples in the Old City of Varanasi, 
before another of Varanasi’s great sights unfolds before you- the nightly Aarti ceremony when thousands of tiny oil lamps are lit 
and set afloat on the waters of the Ganges at dusk, to the accompaniment of prayers, chants, trumpets and cymbals- a truly unforgettable 
experience Overnight in Varanasi- Palace on Ganges</p>

<h3 class="package-heading">Day 17: Varanasi- Khajuraho </h3>
<p>Dawn boat ride on the Ganges to watch as the day’s pilgrimage begins- this is a truly timeless ritual which has been going on 
along the banks of the Ganges here for millennia. Later transfer to the airport and catch the flight IT 331 at 1110 hours to Khajuraho 
where you’ll be met on arrival. Tour the temples of Khajuraho in the afternoon and in the evening attend the Sound and Light show which 
brings the history of this remarkable region to life. Overnight at Usha Bundela </p>

<h3 class="package-heading">Day 18: Khajuraho- Bandhavgarh</h3>
<p>Depart Khajuraho and drive 5 hours to Bandhavgarh, arriving for lunch and then your first safari into the park in the late afternoon. 
Overnight in Bandhavgarh at Jungle Lodge  </p>

<h3 class="package-heading">Day 19: Bandhavgarh </h3>
<p>The forests of Bandhavgarh are home to a staggering array of wildlife and it’s absolutely certain that you’ll see a lot of different 
species on your two safaris today. Accompanied by a naturalist who is familiar with the Park and its inhabitants, you’ll be able to get a 
feel for the diversity of this wonderful area. Of course the ‘prize’ sighting is the Royal Bengal Tiger and with luck you’ll see one or 
more of these majestic cats up close. Overnight at Jungle Lodge Bandhavgarh </p>

<h3 class="package-heading">Day 20: Bandhavgarh- Kanha</h3>
<p>After a fourth and final safari in Bandhavgarh this morning, depart and drive south for 4 hours to reach Kanha by mid-afternoon.
 No safari in Kanha today. Overnight at Jungle Lodge Kanha </p>
 
 <h3 class="package-heading">Day 21: Kanha </h3>
<p>The open grasslands of Kanha are a very different environment to that of Bandhavgarh and offer an even better opportunity to see tigers. 
Other animals seen here include the rare swamp deer, crocodiles, leopards, and many large prey species. Overnight at Jungle Lodge Kanha </p>

 <h3 class="package-heading">Day 22: Kanha- Raipur</h3>
<p>A final safari in the morning before departing for the 5-hour drive to Raipur. Overnight in Raipur at Hotel Celebrations </p>

 <h3 class="package-heading">Day 23: Raipur- Bangalore  </h3>
<p>Transfer to Raipur airport for a flight to Mumbai (Jet Lite S2-378) and then to Bangalore (Jet Konnect 9W-2105) Depart Raipur at 910, 
arrive Mumbai 1045, depart Mumbai 1550, arrive Bangalore 1725- layover is unavoidable). You’ll be met on arrival in Bangalore and drive 3 hours to Mysore. 
Overnight in Mysore at Metropole Hotel</p>


