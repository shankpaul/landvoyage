<h3 class="package-heading">Day 3: Agra</h3>
	 
<p>After breakfast start the guided tour of the great Taj Mahal, Agra Fort and Itmud – u – Daulla’s tomb popularly known as Baby Taj, later day free 
for own activities. Overnight stay at Grand Imperial </p>

<h3 class="package-heading">Day 4: Agra – Fatehpur Sikri – Jaipur (260kms/6hrs) </h3>
	 
<p>After breakfast drive to Jaipur and enroute stop to visit the ruins of great Mughal dynasty called Fatehpur Sikri. On reaching Jaipur, 
check into the hotel and relax for the day. Overnight stay at Hotel Trident </p>

<h3 class="package-heading">Day 5: Jaipur </h3>
	 
<p>After breakfast guided visit to Amber Fort located in the outskirts of Jaipur city. Then visit City Palace Museum, Hawa Mahal (Palace of Wind) 
and Jantar Mantar (ancient scientific observatory). Then visit a rug factory and see the ladies in the intricate work or visit a gem factory and see 
the cutting and polishing work involved. Later free time to walk around and explore the colorful handicraft and textile market. Overnight stay at Trident. </p>

<h3 class="package-heading">Day 6: Jaipur – Shahpura (220kms/3.5hrs) </h3>
	 
<p>After breakfast drive to Shahpura and check into the lovely homestay called Shahpura Bagh. This is a great place to relax during your trip and you 
have the option for birding tour and nature walk. Overnight stay at Shahpura Bagh Heritage Homestay. </p>

<h3 class="package-heading">Day 7: Shahpura - Udaipur (220kms/4hrs)</h3>
	 
<p>After breakfast drive to the romantic city of Udaipur also referred as Venice of the East. The beauty of Udaipur lies in its seven lakes 
which surrounds the city. On reaching Udaipur, check into the hotel and After guided visit to Jagdish temple and City Palace, in the evening enjoy boat 
ride to explore the beauty of Lake Pichola. Overnight stay at Trident </p>

<h3 class="package-heading">Day 8: Udaipur – Jodhpur (280kms/5hrs) </h3>
<p>After breakfast drive to Jodhpur which is referred as Blue City. On arrival check into the hotel and After start the full day guided tour visiting 
Clock tower, Jaswant Thada Cenotaph, Mehrangarh Fort and Umaid Bhavan Palace Museum, later free time for own activities. Overnight stay at Ajit Bhawan </p>

<h3 class="package-heading">Day 9: Jodhpur – Jaisalmer (330kms/6hrs) </h3>
<p>After breakfast drive to Jaisalmer and on arrival check into the hotel and After breakfast guided tour of Gadsisar Lake, Jaisalmer Fort which is 
one of oldest living fort in the world. There are some magnificent Jain temples inside the fort. Then visit the beautifully carved and painted Nathmalji 
Ki Haveli, Salim Singh’s Haveli and Patwon Ki Haveli. Later free time to explore the local market. Overnight stay at Rawal Kot </p>

<h3 class="package-heading">Day 10: Jaisalmer – Manvar (170kms/3hrs) – Jodhpur (120kms/2hrs) – Mumbai (by flight)</h3>
<p>After breakfast drive to a small village called Manvar and After  drive back to Jodhpur to board the Jet Airways flight 9W 2544 at 13.30hrs and you 
shall reach Mumbai at 15.25hrs. </p>


