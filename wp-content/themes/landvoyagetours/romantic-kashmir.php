	  <p>On arrival at leh airport, you will be met by our representative at the airport and escort you to Hotel. 
	  Complete day for the acclimatization which is 3500 Mtrs above sea level. In the evening short walk around Leh bazaar Overnight at Hotel.</p> 
	  
	  <h3 class="package-heading">Day 2: Leh – Monasteries</h3>
	  <p>Day excursion to the famous monasteries, it is interesting to visit early in the morning to enjoy the praying ceremony at Thiksey monastery. 
	  Continue drive to Hemis monastery which is one of the most famous monasteries of the Ladakh region. On the way back visit to Shey Palace. 
	  Overnight at Hotel.</p>

	  
<h3 class="package-heading">Day 3: Leh- Alchi – Uletokpo 75 Kms</h3>
<p>Leave for Uletokpo camping which is one of the most beautiful camping in the Ladakh region on the way visit Spituk monastery which is just 
5 kms from Leh town. Continue drive to Alchi monastery is the only monastery in the Ladakh on flat ground. By the lunch time you will reach Uletokpo. 
Evening enjoy to sit on the bank of river coming down from the Ridzong side. Overnight at Camp.</p>

<h3 class="package-heading">Day 4: Uletokpo – Lamayuru – Leh 180 Kms</h3>
<p>Leave Uletokpo and continue drive to Lamayuru via Khaltsi. From here another 22 Kms drive will take you to Lamayuru. 
The oldest and spectacularly set Lamayuru monastery founded in the 10th Century. Visit the monastery. In the late afternoon drive back to 
Leh which will take about 4/5 hours drive, on the way back we stop at magnetic hill. Overnight at Hotel.</p>

<h3 class="package-heading">Day 5: Leh – Khardongla Pass – Leh 85 Kms</h3>
<p>After breakfast full day excursion to Khardongla pass which is the highest motorable pass in the world 5600 Mtrs and has been recorded in 
the Gunnies Book of world records. At the top enjoy the nature beauty of snow capped mountain. In the evening back to the hotel. Overnight at Hotel.</p>

<h3 class="package-heading">Day 6: Leh – Pangong Lake - Leh (290 Kms)</h3>
<p>Leave early morning at 5AM to Pangong Lake through Changla pass 5486 Mtrs. While ascending Changla pass 5486 Mtrs. 
Enjoy the beautiful panoramic view of the villages below the pass. After crossing the pass halt for picnic lunch at Tsoltak, 
the summer pastureland of the yaks. Arrive Durbuk and from there a short drive takes you to the western shore of the lake. Enjoy 
the landscape in the back drop of the Lake. This famous blue brackish Lake of Pangong is 5/6 Kms wide and over 144 Kms long with 
half of it’s running the other side of the “INDO CHINA BORDER”. One rarely feels so close to nature and environment and the scenery 
is unforgettable. In the late afternoon drive back to Leh. Overnight at Hotel.</p>

<h3 class="package-heading">Day 7: Leh – Delhi</h3>
