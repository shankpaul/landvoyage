<h3 class="package-heading">Day 3: Shimla -Kullu –Manali</h3>
<p>drive to Manali the valley of Gods. Manali is a combination of natural splendour and rich cultural heritage. Enjoy the Scenic Himalayas and the 
Beas river from the window of your cab. On the way you can have  a stop over at  Sunder Nagar Lake, Pandoh dam on river Beas and Kullu if time permits. 
