on number of vehicles. The majesty of the mountains and the glaciers can be seen at their best After enjoying this snowy heaven. Take a sledge ride 
down the slopes & try luck by climbing the little snow hills. On descending to Manali, halt at the Rahalla falls and Solang Valley. The Magnificent 
views and natural beauty are a never-ending source of delight. Also halt at Marhi, Ralha falls, Nehru Kund. Night stay in Manali. 
Rohtang Pass is closed on every Tuesday.</p>

<h3 class="package-heading">Day 5: Manali-Dharamshala</h3>
<p>After breakfast check out from the Hotel & drive to (6-7 hrs) Dharamshala. Arrive & transfer to hotel. </p>

<h3 class="package-heading">Day 6: Dharamshala</h3>
<p>Sightseeing of Dharamshala covering Official residence of Dalai Lama the exiled Tibetan Leader, McLeodganj, Dal lake etc. Night stay in Dalhousie</p>

