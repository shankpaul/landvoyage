	  <h3 class="package-heading">Day 1 Arrival Bangkok – Phuket</h3>
	  <p>Upon your arrival in Phuket you will be transferred to the hotel. This day is free at leisure. You can opt for Phuket Fantaseathis 
	  evening on extra charges. Phuket Fantasea Show is inspired by Thailand's rich and exotic heritage. Multitude of activities and entertainment: 
	  a Carnival village with games, handicrafts and shopping; a 4,000-seat theme restaurant offering a grand buffet of Thai and international cuisines; 
	  and a breathtaking Las Vegas-style theatrical show, where state-of-the-art technology and special effects enhance the grace and beauty of 
	  Thailand's myths, mysteries and magic in a wondrous extravaganza certain to delight the whole family. Overnight in the hotel.</p> 
	  
	  <h3 class="package-heading">Day 2 Phuket half day City tour</h3>
	  
	  <p>After breakfast, we proceed for half day Phuket city tour, As the tour name suggests this is an ideal opportunity to familiarize yourself 
	  with your holiday island and take in the islands natural beauty as we drive along the beach roads through Patong, Karon, Kata and Rawai. 
	  For fabulous photo opportunities we will stop at the Karon viewpoint and cultural sites such as the sacred Buddhist temple of Phuket Wat Chalong. 
	  Phuket town offers an interesting mix of architecture including Sino – Portuguese buildings. This town has been the backdrop for many western movies 
	  as it is said to be very similar to Indochina. Rest of the day is free to explore on your own. Phuket is a shopaholic's paradise! The island has a 
	  great variety of shopping, from bustling open-air village food markets, noisy night markets, street stalls and local shops to western-style department 
	  stores and upmarket specialist shops. Prices naturally vary, going from amazing bargains to credit card-melting designer labels. Located downtown, 
	  diagonally opposite Bangla Road, the Jungceylon Shopping Complex has radically changed the face of shopping in Patong and is one of the major shopping 
	  attractions in Phuket. Overnight in the hotel.</p>

	  
<h3 class="package-heading">Day 3 Phuket day free. Optional James bond Island tour or Phi Phi Island Tour</h3>
	 
<p>Today after breakfast, this day is at leisure for you, We recommend you to book James Bond Island tour or Phi Phi Island tour. 
Joining a James Bond Island tour and cruise the Phang Nga Bay is the most popular activity in the region. It consists of numerous 
dramatic limestone cliffs rising sheer out of the waters. The most famous one being James Bond Island with its needle formed limestone 
rock which featured in the James Bond movie "The Man with the Golden Gun". The Muslim fishing village on the island Koh Panyee is another 
popular spot to visit. Canoeing through limestone caves into mangrove forests is included in the tours. Phi Phi Islands are famous for its 
limestone cliffs rising sheer out of the emerald green waters, white powdery beaches and its many coral reefs offering great diving and snorkeling. 
Overnight stay at hotel</p>

<h3 class="package-heading">Day 4 Phuket- Bangkok – Pattaya transfer</h3>
	 
<p>After breakfast, you will be transferred to the airport to board your flight to Bangkok. Upon your arrival in Bangkok you will be transferred to Pattaya, 
which is a short 2 hours drive from Bangkok. We take you to your hotel, and overnight at Hotel in Pattaya</p>

<h3 class="package-heading">Day 5 Pattaya-Coral island with Lunch</h3>
	 
<p>After breakfast, you will transfer to the sparkling blue water of the Gulf of Thailand by a speedboat and arrive at the idyllic Coral Island 
(Koh Lan), where you will spend the day luxuriating under the welcoming tropical sun. You can lounge, swim, snorkel or simply enjoy the sun at the 
islands immaculate golden beaches. You all have the option of viewing the beautiful coral formations around the island either through a glass-bottom boat 
or by diving in. Upon your return from the island, you will enjoy a scrumptious Indian lunch. Coral Island tour on join/shared speed boat will stop for 
Parasailing for those who are interested. Those who are not interested in Parasailing will wait for the rest to finish the activity. In the evening, go 
for optional Alcazar Show.(at extra cost). The Alcazar Show a world-famous cabaret, which can give the Lido and Moulin Rouge of Paris, a run for their money, 
featuring a grand theatre with 1200 seats and thousands of watts of pulsating sound. Truly, a visit to the Alcazar Show could well become the highlight of 
your far-eastern journey. Overnight in the hotel in Pattaya.</p>

<h3 class="package-heading">Day 6 Pattaya – Bangkok City tour</h3>
	 
<p>Today after breakfast, you will check out from your Pattaya hotel and proceed for Half-day city tour of Bangkok on a coach. Bangkok takes great pride 
in a large number of fascinating temples around the capital. The major ones normally visited are Golden Buddha temple, Wat Traimit, housing a five-and-half 
ton of Golden Buddha statue; the Marble Temple, to a Thai traditional massage school, the tour will give you an in-depth understanding of the traditions and 
culture of Bangkok. After sightseeing you check in to your Bangkok hotel. The evening is free at your own for shopping. Bangkok is famous for electronics and 
best clothes. Overnight stay at hotel.</p>

<h3 class="package-heading">Day 7 Bangkok day free</h3>
	 
<p>After a buffet breakfast the day is free at leisure. We suggest you to book a tour of Safari World & Marine Park.(Optional at extra cost). 
Safari World & Marine Park is Thailand's greatest open zoo, and is an experience like none other. The zoo is divided into four sections, each with 
its own variety of animals and shows. The zoo has orangutans, zebras, lions, birds, giraffes and a host of other amazing creatures. There is also a 
dolphin & sea lion show which is not to be missed. In the afternoon, have lunch at the park, and proceed to the hotel in the evening. Overnight at hotel</p>

<h3 class="package-heading">Day 8 Depart –Bangkok</h3>
	 
<p>Have a buffet breakfast at the hotel in the morning. After check out formalities, you will be picked up for departure transfer to the airport for 
flight way back home.</p>


