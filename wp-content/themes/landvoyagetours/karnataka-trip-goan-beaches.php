	  <h3 class="package-heading">Day 1 : Arrival Goa</h3>
	  <p>Today arrive at the Goa Airport where you will be received by our tour executive, who will introduce the driver. 
	  Later check in the hotel refresh and rest of the day is at leisure to enjoy the beautiful beaches and explore Old Goa. 
	  Overnight stay at Panjim Inn, Goa</p> 
	  
	  <h3 class="package-heading">Day 2 : Goa</h3>
	  <p>Today you can spend in Goa beaches and enjoy the beauty of Indian beaches. Overnight stay at Panjim Inn, Goa</p>

	  
	<h3 class="package-heading">Day 3 –Goa -  Badami</h3>
<p>Morning after breakfast you will drive to Badami on reaching the hotel check in and relax the day at leisure. 
Overnight stay at The Heritage Resort, Badami</p>

<h3 class="package-heading">Day 4 : Badami- Aihole – Pattadakal – Badami</h3>
<p>This day city tour of Badami visit Badami caves and the number of temples that are located near by the lake, The chain of cave temples 
in Badami are fabulous example of cave architecture next to Ajanta & Ellora here you can witness the full range of religious sects. Back in Badami, 
you are free to explore the town on your own. Overnight stay at The Heritage Resort, Badami</p>

<h3 class="package-heading">Day 5 :Badami – Hospet</h3>
<p>Morning after breakfast drive to Hospet, on the way visit Aihole and Pattadakal that includes sightseeing of the famous Archeological museum, 
Virupaksha temple and Malikarjuna temple.. Aihole is where you will see the evolution of Hindu temple architecture from the early shrines of the 
Lad Khan Temple to more complex structures like that of the Meguti Temple. Pattadakal, once the second capital of the Badami Chalukyans dynasty, 
was also the playground of Hindu temple architects. After reaching the hotel check in and relax the day at leisure. Overnight stay at Malligi Hotel, 
Hospet</p>

<h3 class="package-heading">Day 6 : Hospet(Excursion to Hampi)</h3>
<p>This day after breakfast drive to Hampi on a day excursion and visit Vithala temple, Virupaksha temple, Hazara Rama temple, Old Palace temple 
and Lakshmi Narasimha temple. The 25 Sq Kms of the ruins of Hampi will overwhelm you with its Glory of the bygone era of the Vijayanagara Kingdom. 
Overnight stay at Malligi Hotel, Hospet</p>

<h3 class="package-heading">Day 7 : Hospet – Hassan (Excursion to Belur & Halebid) </h3>
<p>Today will have a long trip towards Hassan which will be a very hectic trip due to the very bad road conditions, so prepare yourself both 
mentally and physically for a long day in road. After your arrival in Hassan relax yourself in your hotel in Hassan. Overnight stay at Hoysala Village, 
Hassan</p>

<h3 class="package-heading">Day 8: Hassan – Mysore</h3>
<p>Today morning visit the Belur & Halebid temples, which are places of architectural wonders, where the sightseeing includes Chennakeshava temple, 
Hoysaleshwara temple, Jain temples and have a quick glimpse at Chennigaraya, Veeranarayana, Sridevi, and Bhoodevi temples which are all located in the 
same complex.  Later drive towards Mysore. Overnight stay at Royal Orchid Metropole, Mysore</p>

<h3 class="package-heading">Day 9: Mysore – Sightseeing</h3>
<p>Today morning travel to the village of Somnathpur located on the banks of the Kaveri River. The village is famous for its beautiful and well 
preserved Keshava temple. The temple is enclosed in a walled courtyard and is built on an elevated star shaped platform. The temple is a fine example 
of a temple built by the Hoysalas and has the trademark intricate sculpture work. Later you can  continue your tour by returning to Mysore for further 
sightseeing. You will be enthralled by Mysore palace with its displays of art from all around the world. Located on top of Chamundi Hill the tour 
continues by a visit to The Chamundeeswari temple. It houses an impressive sculpture of a bull carved out of a single granite rock which is 4.57 meters 
in height and 6.10 meters in length. Later finish your tour by a visit to the Devaraja Market with all its colorful flowers, fruits, and vegetables. 
Overnight stay at Royal Orchid Metropole, Mysore</p>

<h3 class="package-heading">Day 10 : Mysore – Ooty</h3>
<p>Today morning drive to the famous hill station Ooty, On reaching Ooty the "Queen of hill stations" a land of dreams and a picturesque hill station, 
make a visit to Botanical garden, Sim's park and Dodabetta peak and evening enjoy at the Ooty lake. Overnight stay at Sullivan Court, Ooty</p>

