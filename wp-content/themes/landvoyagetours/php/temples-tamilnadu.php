	  <h3 class="package-heading">Day 1: Arrival Chennai </h3>
	  <p>Arrival Chennai by IC-542 at 15:30</p>
	<p>	On arrival at Chennai, our Tour Executive will welcome you at the airport and then transfer to the hotel. If time permits visit to, 
	Kapaleeshwar temple, Marina Beach, Santhome Church,  Paartha Sarathy church (if time permits both you can see) and later if you are interested 
	you can do Shopping over there.	</p> 
	  
	  <h3 class="package-heading">Day 2: Chennai – Mahabalipuram – Pondicherry (160 Km -3 ½ hours drive)</h3>
	  <p>After breakfast drive to Mahabalipuram; visit to Five Rathas,  Arjuna’s Penance,  Shore Temples,  Caves, Then drive to Pondicherry; 
	  check into the hotel and relax. Later visit to Auroville, Aurobhindo Ashram,  Manakkula Vinayagar temple and Beach </p>

	  
	<h3 class="package-heading">Day 3: Pondicherry – Chidambaram – Kumbakonam (180 Km -4 hours drive)</h3>
<p> After breakfast drive to Chidambaram; visit to Natarajar temple (one of the five elements – sky) Then drive to Kumbakonam; check into the 
hotel and relax. Later visit to Dharasuram Temple (UNESCO site),  Sarangabani temple, Kumbeshwar temple. In Tanjore region, there are no 4 star hotels; 
only 3 star hotels, but expensive. So I have chosen a nice 3 star accommodation for you in Kumbakonam in stead of Tanjore in both the options. 
It is less expensive than Tanjore 3 star hotels. The distance from Tanjore to Kumbakonam is just 37 Km only.</p>

<h3 class="package-heading">Day 4: Kumbakonam – Tanjore – Trichy (110 Km -2 ½ hours drive)</h3>
<p>After breakfast drive to Tanjore; then visit to Brahadeeshwar temple (UNESCO site), Palace 
Then continue on to Trichy; visit to Srirangam Ranganathar temple (it has the tallest temple tower in the world), Rock Fort temple, 
Thiruvanaikoil temple (one of the Five Elements – Water)</p>

<h3 class="package-heading">Day 5: Trichy – Madurai (135 Km -2 ½ hours drive)</h3>
<p>After breakfast drive to Madurai; visit to  Meenakshi Amman temple, Thirumalai Naicker Mahal, Mariamman tank. If time permits visit to Museum & other 
temples in Madurai</p>

<h3 class="package-heading">Day 6: Madurai –Kanyakumari (250 Km -5 hours drive)</h3>
<p>After breakfast drive to Kanyakumari It is at the Southern most tip of the Indian Peninsula where the Arabian Sea, the Indian Ocean and the Bay of 
Bengal meet and hence the confluence is called Thiriveni Sangamam. It is also known by its former name Cape Comorin. This is the only place in India 
where the Sunset and Moonrise can be viewed simultaneously on a full moon day. Check into the hotel and relax for a while. Later visit to Kumari Amman 
temple (Hindus only allowed), Vivekananda Rock , Gandhi Memorial and evening Sun Set </p>

<h3 class="package-heading">Day 7: Kanyakumari – Trivandrum departure (100 Km -2 ½ hours drive)</h3>
<p>After breakfast drive to Trivamdrum; enroute visit to Padmanabhapuram Palace, If you are interested, you can do visit to Padmanabha Swamy temple 
(Hindus only allowed) Art Gallery, Museum and later drive to Trivandrum Airport </p>




