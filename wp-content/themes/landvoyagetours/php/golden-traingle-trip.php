      <div class="col-md-12">
	  <h3 class="package-heading">Day1: Delhi Arrival </h3>
	  <p>On arrival at Delhi airport, you will be received by our company representative and assisted to the hotel for relax. 
	  After lunch drive 30mins to start the guided tour of Old Delhi visiting the historical monuments like Red Fort and Jama Masjid 
	  which is the largest mosque in India. Experience a cycle rickshaw ride driving in the narrow and colorful street of Chandini Chowk. 
	  Later visit the spice market and textile shops at Chandini Chowk. Overnight stay at Florence Inn</p> 
	  
	  <h3 class="package-heading">Day 2: Delhi</h3>
	  
	  <p>After breakfast guided sightseeing tour of New Delhi visiting the historical monuments like Qutub Minar, Humayun’s Tomb, 
	  Indira Gandhi Memorial Museum where the photos and belongings of India’s first and only lady Prime Minister has been preserved, 
	  Raj Ghat where our nations father Mahatma Gandhi is cremated and the Akshardham temple. Then drive passing through the Parliament, 
	  President’s house and India Gate. Later visit Delhi Haat – a food joint & crafts market and Connaught place which has a Central Park 
	  and all types of restaurant with different food and a movie theatre. Overnight stay at Florence Inn </p>

	  
<h3 class="package-heading">Day 3: Delhi – Agra (200kms/4hrs)</h3>
	 
<p>After breakfast drive to Agra enroute visiting Sikandra to see the tomb of the great Mughal Emperor Akbar. On reaching Agra, check into the 
homestay and relax for some time. After lunch start the Agra guided tour visiting Agra Fort, Itmud – u Daulla’s tomb popularly known as Baby Taj 
and the world famous Taj Mahal built by Emperor Shajahan as a symbol of love for his beloved wife Mumtaz. Overnight stay at Garden Villa Homestay.</p>

<h3 class="package-heading">Day 4: Agra – Fatehpur Sikri – Ranthambore (280kms/7hrs)</h3>
	 
<p>After breakfast drive to Ranthambore enroute stop at Fatehpur Sikri and witness the ruins of the once great Mughal Dynasty. Ranthambore National 
park is famous for tigers and is one of the best places in the country to see these majestic predators in the wild. You can also site deer, jackal, 
mongoose, sloth bear, leopard, etc. Check into the jungle resort and in the evening option for jungle safari. Overnight stay at Ranthambore Forest 
Resort </p>

<h3 class="package-heading">Day 5: Ranthambore – Jaipur (180kms/4hrs)</h3>
 
<p>Early morning option for jungle safari once again and after breakfast drive to the capital city of Rajasthan called Jaipur. 
Jaipur is also referred as Pink city as all the buildings in the old town are painted in pink. Check into the guesthouse and relax, 
later free to explore the city on your own. Overnight stay at Dera Rawatsar Guesthouse</p>

<h3 class="package-heading">Day 6: Jaipur</h3>
	 
<p>After breakfast start the full day guided tour visiting the Museum in City Palace, Jantar Mantar (ancient scientific observatory) and Hawa Mahal 
(palace of wind). Later excursion trip to Amber Fort where you can enjoy elephant ride. Overnight stay at Dera Rawatsar </p>

<h3 class="package-heading">Day 7: Jaipur – Delhi Departure (260kms/6hrs) </h3>
	 
<p>After breakfast drive back to Delhi, check into the hotel and relax for the day the late evening after dinner you will be given departure 
assistance to Delhi international terminal to board your flight. </p>




